using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BoxField
{
    class Box
    {
        public int x, y;

        /// <summary>
        /// Constructor Method for a box
        /// </summary>
        /// <param name="_x">X position</param>
        /// <param name="_y">Y position</param>
        public Box (int _x, int _y)
        {
            x = _x;
            y = _y;
        }

    }
}
